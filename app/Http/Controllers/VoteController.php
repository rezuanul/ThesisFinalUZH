    {
        $auth = Auth::user();
        
        $vote = $auth->votes()->create([
            'blockchain_proposal_id' => $request->blockchain_proposal_id,
            'wallet_address' => $auth->wallet_address,
            'proposal_id' => null,
            'option' => $request->option,
            'meta' => ['transaction_id' => $request->transaction_id],
        ]);

        return Redirect::back()->with('success', 'You have successfully voted.');
